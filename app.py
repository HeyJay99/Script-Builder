import streamlit as st

st.set_page_config(
    page_title="숏폼 대본 생성기",
    page_icon="🎬",
    layout="centered",
)

st.title("🎬 숏폼 광고 대본 생성기")
st.markdown(
    """
광고 소재용 숏폼 영상 대본을 자동으로 만들어주는 도구입니다.

---

### 사용 방법

**📝 대본 생성** 페이지
1. 왼쪽 사이드바에서 저장된 업체를 선택하거나 새로 입력합니다.
2. 이번 영상의 **주요 소구**를 입력합니다.
3. 레퍼런스 대본을 선택하거나 업로드합니다.
4. **대본 생성** 버튼을 누릅니다.
5. 생성된 대본을 다운로드하거나 레퍼런스로 저장할 수 있습니다.

**🎬 스크립트 추출** 페이지
1. 경쟁사 광고 영상이나 참고 오디오를 업로드합니다.
2. **스크립트 추출** 버튼을 누릅니다.
3. 추출된 대본을 레퍼런스로 저장하면 대본 생성 페이지에서 바로 사용할 수 있습니다.

---

### 시작하기

왼쪽 사이드바에서 페이지를 선택하세요.
"""
)

st.info(
    "처음 사용 시 `.env` 파일에 `GEMINI_API_KEY`, `SUPABASE_URL`, `SUPABASE_KEY`를 설정해야 합니다.",
    icon="ℹ️",
)
